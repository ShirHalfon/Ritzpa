
public class UserItem {

    private String      symbol;
    private int         amount;

    public String getSymbol() {
        return symbol;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }
}
